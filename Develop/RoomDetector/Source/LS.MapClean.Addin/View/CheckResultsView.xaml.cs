namespace LS.MapClean.Addin.View
{
    /// <summary>
    /// Interaction logic for CheckResultsView.xaml
    /// </summary>
    public partial class CheckResultsView : UserControl
    {
        public CheckResultsView()
        {
            InitializeComponent();
        }

        // http://stackoverflow.com/questions/592373/select-treeview-node-on-right-click-before-displaying-contextmenu
        private void OnPreviewMouseRightButtonDown(object sender, MouseButtonEventArgs e)
        {
            TreeViewItem treeViewItem = VisualUpwardSearch(e.OriginalSource as DependencyObject);

            if (treeViewItem != null)
            {
                treeViewItem.Focus();
                e.Handled = true;
            }
        }

        static TreeViewItem VisualUpwardSearch(DependencyObject source)
        {
            while (source != null && !(source is TreeViewItem))
                source = VisualTreeHelper.GetParent(source);

            return source as TreeViewItem;
        }
    }
}
